// If-Else Statement
let condition = true;
if (condition) {
    console.log('Condition is true');
} else {
    console.log('Condition is false');
}

// Switch Statement
let day = 'Monday';
switch (day) {
    case 'Monday':
        console.log('It\'s the start of the week');
        break;
    case 'Friday':
        console.log('It\'s almost the weekend');
        break;
    default:
        console.log('It\'s a regular day');
}
